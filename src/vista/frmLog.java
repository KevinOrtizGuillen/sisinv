    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        // TODO add your handling code here:
        String user=txtusuario.getText();
        //String pin=DigestUtils.sha1Hex(txtcontrasena.getText());
        String pin=txtcontrasena.getText();
        mu.setUsua_usuar(user);
        mu.setPin_usuar(pin);
        usuario=cu.login(mu);         
        if (cu.totaldeResgistros>0) {            
          JOptionPane.showMessageDialog(null, "Usuario autenticado correctamente");
          this.dispose();
          frmPrincipal form = new frmPrincipal();
          form.toFront();
          form.setVisible(true);
          //
          form.lblIdUsua.setText(usuario.getValueAt(0,0).toString());
          form.lblDniUsua.setText(usuario.getValueAt(0,1).toString());          
          form.lblNomb_Usua.setText(usuario.getValueAt(0,3).toString()+" "+usuario.getValueAt(0,4).toString()+", "+usuario.getValueAt(0,2).toString());
          form.lblCorreoUsua.setText(usuario.getValueAt(0,5).toString());
          form.lblTipoUsua.setText(usuario.getValueAt(0,7).toString());
          form.lblEstadoUsua.setText(usuario.getValueAt(0,9).toString());
                           
        }else{
            JOptionPane.showMessageDialog(null,"No tiene permiso para ingresar al sistema ");           
        }
        
    }//GEN-LAST:event_btningresarActionPerformed

