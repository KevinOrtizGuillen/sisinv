    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNombr.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre de la marca");
            txtNombr.requestFocus();
            return;
         }
         if (txaDescrip.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar alguan descripci√≥n de la marca");
            txaDescrip.requestFocus();
            return;
         }         
        inicializarObj();
        if(opcion==0){
        guardar();
        }else{
        editar();
        }
        inhabilitar();
        mostrarMarcas("");
    }//GEN-LAST:event_btnGuardarActionPerformed

